### WORKING WITH DATA
# :: means we are using the chickwts function, or module, or _something_ from the datasets package
chickwts <- datasets::chickwts

# change column dtype character->factor
chickwts$feed <- as.factor(chickwts$feed)

# subset chickwts for only entries with feed type horsebean
chickwts[which(chickwts$feed == 'horsebean'),]

#get unique values of a column
feed_types <- unique(chickwts$feed)

# init empty list and index counter for for-loop
feed_subsets <- list()
i <- 1

# for loop to go through feed_types and create subsets of data for each feed type
for (ftype in feed_types) {
  feed_subsets[[i]] <- chickwts[ which(chickwts$feed == ftype),]
  i <- (i + 1)
}

# init index counter for for-loop
i <- 1
# for-loop to create and assign new variables to the data.frame objs generated by the previous for-loop
for (ftype in feed_types) {
  # add '_df' name extension to current ftype string
  ftype <- paste(ftype, '_df', sep='')
  # create new variable called ftype_df and assign ftype subset of chickwts to it
  assign(ftype, value=feed_subsets[[i]])
  i <- (i + 1)
}



